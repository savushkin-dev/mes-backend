CREATE TABLE IF NOT EXISTS reportserver.BD_REFRESH_TOKENS
(
    F_ID        BIGSERIAL PRIMARY KEY,
    TOKEN       VARCHAR(255) NOT NULL UNIQUE,
    EXPIRY_DATE TIMESTAMP    NOT NULL,
    USER_ID     INT          NOT NULL,
    REVOKED     BOOLEAN      NOT NULL DEFAULT FALSE
);


CREATE TABLE reportserver.BD_USR
(
    F_ID      SERIAL PRIMARY KEY,
    USERNAME  VARCHAR(100) NOT NULL,
    PASSWORD  VARCHAR      NOT NULL,
    AUTH_TYPE VARCHAR(10)  NOT NULL CHECK (AUTH_TYPE IN ('NTLM', 'STANDARD')),
    ENABLED   BOOLEAN      NOT NULL DEFAULT TRUE
);


CREATE TABLE reportserver.BD_ROLES
(
    F_ID SERIAL PRIMARY KEY,
    NAME VARCHAR(50) NOT NULL UNIQUE
);


CREATE TABLE reportserver.USER_ROLES
(
    USER_ID INTEGER REFERENCES BD_USR (F_ID) ON DELETE CASCADE,
    ROLE_ID INTEGER REFERENCES BD_ROLES (F_ID) ON DELETE CASCADE,
    PRIMARY KEY (USER_ID, ROLE_ID)
);


CREATE TABLE reportserver.BD_REPORT
(
    ID                     SERIAL PRIMARY KEY,
    REPORT_NAME            VARCHAR(30) NOT NULL,
    REPORT_CATEGORY        VARCHAR(30) NOT NULL,
    DB_URL                 VARCHAR,
    DB_USERNAME            VARCHAR(30),
    DB_PASSWORD            VARCHAR(30),
    DB_DRIVER              VARCHAR,
    SQL                    VARCHAR,
    CONTENT                VARCHAR,
    STYLES                 VARCHAR,
    PARAMETERS             VARCHAR,
    SCRIPT                 VARCHAR,
    IS_SQL                 BOOLEAN,
    DATA_BANDS             VARCHAR,
    IS_BOOK_ORIENTATION    BOOLEAN,
    LAYOUT_SETTINGS_PARAMS VARCHAR,
    LAYOUT_PARAMS          VARCHAR,
    CONSTRAINT uk_report_category_name UNIQUE (REPORT_CATEGORY, REPORT_NAME)
);


CREATE TABLE reportserver.BD_REPORT_GLOBALS
(
    ID          SERIAL PRIMARY KEY,
    KEY         VARCHAR(50) NOT NULL UNIQUE,
    VALUE       VARCHAR DEFAULT '',
    DESCRIPTION VARCHAR DEFAULT ''
);

CREATE TABLE IF NOT EXISTS REPORT_ACCESS_LOG (
                                                 ID BIGSERIAL PRIMARY KEY,
                                                 CATEGORY VARCHAR(255) NOT NULL,
                                                 REPORT_NAME VARCHAR(255) NOT NULL,
                                                 ACCESS_TIME TIMESTAMP NOT NULL
);

CREATE INDEX IF NOT EXISTS IDX_REPORT_ACCESS_LOG_ACCESS_TIME ON REPORT_ACCESS_LOG(ACCESS_TIME);

